function [condition_names, baseline_condition] = gamma_get_condition_names(session_number)
% [condition_names, baseline_condition] = ...
%       gamma_get_condition_names(session_number)
%
% Condition names for MEG Gamma experiment
%
% Inputs: 
%   session_number: integer, for example, the session number for this
%                             folder is 4: 04_Gamma_7_23_2014_subj013
% Outputs:
%  condition_names: cell array of condition names
%
% Example: condition_names = gamma_get_condition_names(5)
%

% TODO fix for session_number <= 3

if session_number < 10
    condition_group = 1;
elseif session_number >= 10 && session_number < 16
    condition_group = 2;
else 
    condition_group = 3;
end


switch condition_group
    case 1
        condition_names               = {   ...
    'White Noise' ...
    'Binary White Noise' ...
    'Pink Noise' ...
    'Brown Noise' ...
    'Gratings(0.36 cpd)' ...
    'Gratings(0.73 cpd)' ...
    'Gratings(1.45 cpd)' ...
    'Gratings(2.90 cpd)' ...
    'Plaid'...
    'Blank'};

    case 2
        condition_names               = {   ...
    'White Noise' ...
    'Binary White Noise' ...
    'Binary Pink Noise' ...
    'Binary Brown Noise' ...
    'Gratings(0.36 cpd)' ...
    'Gratings(0.73 cpd)' ...
    'Gratings(1.45 cpd)' ...
    'Gratings(2.90 cpd)' ...
    'Plaid'...
    'Blank'};

    case 3 
        condition_names               = {   ...
    'House(High Contrast)' ...
    'House(Mid Contrast)' ...
    'House(Low Contrast)' ...
    'Face(High Contrast)' ...
    'Face(Mid Contrast)' ...
    'Face(Low Contrast)' ...
    'Pink Noise(High Contrast)' ...
    'Pink Noise(Mid Contrast)' ...
    'Pink Noise(Low Contrast)'...
    'Gratings(64cpi High Contrast)'...
    'Gratings(64cpi Mid Contrast)'...
    'Gratings(64cpi Low Contrast)'...
    'Blank'};

end
        


baseline_condition = find(~cellfun(@isempty,strfind(condition_names, 'Blank')));

return
